#include "3-calc.h"
/**
  *op_add - adds two integers
  *@a: - first param
  *@b: - second param
  *Return: - returns total sum
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  *op_sub - subracts two integers
  *@a: - first param
  *@b: - second param
  *Return: - returns int
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - multiplies two integers
*@a: - first param
*@b: - second param
*Return: - returns multiplied int
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - divides two integers
*@a: - first param
*@b: - second param
*Return: - returns divided int
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - finds modulo between two integers
*@a: - first param
*@b: - second param
*Return: - returns remainder int
*/
int op_mod(int a, int b)
{
	return (a % b);
}
